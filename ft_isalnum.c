#include "libft.h"

int	ft_isalnum(int c)
{
	int	check;

	check = 0;
	if (c >= 'a' && c <= 'z')
		check = 1;
	else if (c >= 'A' && c <= 'Z')
		check = 1;
	else if (c >= '0' && c <= '9')
		check = 1;
	return (check);
}
