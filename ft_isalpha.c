#include "libft.h"

int	ft_isalpha(int c)
{
	int	check;

	check = 0;
	if (c >= 'a' && c <= 'z')
		check = 1;
	else if (c >= 'A' && c <= 'Z')
		check = 1;
	return (check);
}
